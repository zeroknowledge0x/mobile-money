export function normalizeMccCode(value: string): string {
  return value.trim();
}

export function isValidMerchantMCC(value: string | undefined | null): boolean {
  if (!value) return false;
  const normalized = normalizeMccCode(value);
  return /^[0-9]{4}$/.test(normalized) && ALLOWED_MCC_CODES.has(normalized);
}

export function requireValidMerchantMCC(value?: string | null): string {
  if (!value) {
    throw new Error("Merchant accounts require a valid MCC code.");
  }

  const normalized = normalizeMccCode(value);
  if (!/^[0-9]{4}$/.test(normalized) || !ALLOWED_MCC_CODES.has(normalized)) {
    throw new Error(
      `Invalid Merchant MCC code '${value}'. Allowed codes must be one of: ${Array.from(ALLOWED_MCC_CODES).sort().join(", ")}`,
    );
  }

  return normalized;
}
